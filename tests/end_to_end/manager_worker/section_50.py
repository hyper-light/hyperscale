async def validate_50_3_gc_pause_false_positive() -> None:
    spec = _build_spec(
        "manager_worker_50_3_gc_pause_false_positive",
        "50.3 GC pause false positive - Health monitor tolerates GC pause",
    )
    runner = ScenarioRunner(WORKFLOW_REGISTRY)
    outcome = await runner.run(spec, cleanup=False)
    runtime = _require_runtime(outcome)
    try:
        if outcome.result != ScenarioResult.PASSED:
            raise AssertionError(outcome.error or "Scenario failed")
        manager = _get_manager(runtime, "DC-A")
        assert manager._health_monitor is not None, "GC pause expected health monitor"
    finally:
        await runtime.stop_cluster()


async def run() -> None:
    await validate_50_1_health_restored_mid_dispatch()
    await validate_50_2_zombie_late_progress()
    await validate_50_3_gc_pause_false_positive()


if __name__ == "__main__":
    asyncio.run(run())
