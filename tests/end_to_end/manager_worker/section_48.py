async def run() -> None:
    await validate_48_1_progress_buffer_overflow_recovery()
    await validate_48_2_progress_jitter_smoothing()
    await validate_48_3_backpressure_deescalation_hysteresis()


if __name__ == "__main__":
    asyncio.run(run())
